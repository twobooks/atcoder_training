N,M = map(int,input().split())
arrAB = []
for i in range(N):
    arrAB.append(list(map(int,input().split())))

arrAB = sorted(arrAB, key=lambda lis: lis[0])

drink = M
ans = 0
for lis in arrAB:
    if drink<=0:
        break
    num = min(drink,lis[1])
    ans += lis[0] * num
    drink -= num

print(ans)
